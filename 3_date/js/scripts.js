const now = new Date();
console.log('now', now);

now.getHours() //Get the hour (0-23)
console.log('now.getHours()', now.getHours());

now.getMinutes() // Get the minute (0-59)
console.log('now.getMinutes()', now.getMinutes());

now.getDay() // Get the day as number (0-6)
console.log('now.getDay()', now.getDay());

now.getFullYear() // Get Year as 2019
console.log('now.getFullYear()', now.getFullYear());